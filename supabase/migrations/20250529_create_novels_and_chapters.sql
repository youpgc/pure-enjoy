-- 创建小说表
CREATE TABLE IF NOT EXISTS public.novels (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
    title VARCHAR(255) NOT NULL,
    author VARCHAR(100),
    cover_url TEXT,
    description TEXT,
    category VARCHAR(50),
    source VARCHAR(50) DEFAULT 'original',
    source_url TEXT,
    tags TEXT[],
    chapter_count INTEGER DEFAULT 0,
    word_count INTEGER,
    status VARCHAR(20) DEFAULT 'ongoing', -- ongoing, completed
    is_free BOOLEAN DEFAULT true,
    price DECIMAL(10, 2),
    rating DECIMAL(3, 2),
    read_count INTEGER DEFAULT 0,
    collect_count INTEGER DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 创建章节表
CREATE TABLE IF NOT EXISTS public.chapters (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    novel_id UUID NOT NULL REFERENCES public.novels(id) ON DELETE CASCADE,
    title VARCHAR(255) NOT NULL,
    content TEXT NOT NULL,
    chapter_num INTEGER NOT NULL,
    word_count INTEGER,
    is_free BOOLEAN DEFAULT true,
    price DECIMAL(10, 2),
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(novel_id, chapter_num)
);

-- 创建索引
CREATE INDEX IF NOT EXISTS idx_novels_category ON public.novels(category);
CREATE INDEX IF NOT EXISTS idx_novels_status ON public.novels(status);
CREATE INDEX IF NOT EXISTS idx_novels_created_at ON public.novels(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_chapters_novel_id ON public.chapters(novel_id);
CREATE INDEX IF NOT EXISTS idx_chapters_chapter_num ON public.chapters(chapter_num);

-- 启用 RLS
ALTER TABLE public.novels ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.chapters ENABLE ROW LEVEL SECURITY;

--  novels 表策略：所有人可读，仅创建者可写
CREATE POLICY "novels_select_policy" ON public.novels
    FOR SELECT USING (true);

CREATE POLICY "novels_insert_policy" ON public.novels
    FOR INSERT WITH CHECK (auth.uid() = user_id OR user_id IS NULL);

CREATE POLICY "novels_update_policy" ON public.novels
    FOR UPDATE USING (auth.uid() = user_id);

CREATE POLICY "novels_delete_policy" ON public.novels
    FOR DELETE USING (auth.uid() = user_id);

-- chapters 表策略：所有人可读，仅小说创建者可写
CREATE POLICY "chapters_select_policy" ON public.chapters
    FOR SELECT USING (true);

CREATE POLICY "chapters_insert_policy" ON public.chapters
    FOR INSERT WITH CHECK (
        EXISTS (
            SELECT 1 FROM public.novels n 
            WHERE n.id = chapters.novel_id 
            AND (n.user_id = auth.uid() OR n.user_id IS NULL)
        )
    );

CREATE POLICY "chapters_update_policy" ON public.chapters
    FOR UPDATE USING (
        EXISTS (
            SELECT 1 FROM public.novels n 
            WHERE n.id = chapters.novel_id 
            AND n.user_id = auth.uid()
        )
    );

CREATE POLICY "chapters_delete_policy" ON public.chapters
    FOR DELETE USING (
        EXISTS (
            SELECT 1 FROM public.novels n 
            WHERE n.id = chapters.novel_id 
            AND n.user_id = auth.uid()
        )
    );

-- 更新章节数触发器
CREATE OR REPLACE FUNCTION update_novel_chapter_count()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'INSERT' THEN
        UPDATE public.novels 
        SET chapter_count = chapter_count + 1,
            updated_at = NOW()
        WHERE id = NEW.novel_id;
        RETURN NEW;
    ELSIF TG_OP = 'DELETE' THEN
        UPDATE public.novels 
        SET chapter_count = chapter_count - 1,
            updated_at = NOW()
        WHERE id = OLD.novel_id;
        RETURN OLD;
    END IF;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_update_chapter_count ON public.chapters;
CREATE TRIGGER trigger_update_chapter_count
    AFTER INSERT OR DELETE ON public.chapters
    FOR EACH ROW
    EXECUTE FUNCTION update_novel_chapter_count();

